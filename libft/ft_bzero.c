#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t	i;
	char	*str2;

	i = 0;
	if (!str && !n)
		return ;
	if (n > 0)
	{
		str2 = str;
		while (i < n)
		{
			str2[i] = 0;
			i++;
		}
	}
}
